package org.cloudcoder.progsnap2;

public class EventInitiator {
    public static EventInitiator User = new EventInitiator("User");
    public static EventInitiator Tool = new EventInitiator("Tool");
    public static EventInitiator Instructor = new EventInitiator("Instructor");
    public static EventInitiator TeamMember = new EventInitiator("TeamMember");

    private EventInitiator(String value) {
        this.value = value;
    }

    public String getValue() {
        return value;
    }

    @Override
    public String toString() {
        return value;
    }

    private String value;
}