package org.cloudcoder.app.shared.model;

/**
 * Interface for an object that creates instances of an object
 * of a particular type.
 * 
 * @author David Hovemeyer
 *
 * @param <E> the type of object the factory creates
 */
public interface IFactory<E> {
	/**
	 * Create an object.
	 * 
	 * @return the created object
	 */
	public E create();
}
